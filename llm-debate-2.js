let ws;
let lastSent = 0;
let idleTimer = null;
let usedIndices = new Set();

function hashCode(str) {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash + str.charCodeAt(i)) | 0;
  }
  return Math.abs(hash);
}

function pickResponse(msg) {
  const h = hashCode(msg);
  const total = responses.length;
  // Try to pick unused, fall back to any
  for (let offset = 0; offset < total; offset++) {
    const idx = (h + offset) % total;
    if (!usedIndices.has(idx)) {
      usedIndices.add(idx);
      if (usedIndices.size >= total) usedIndices.clear();
      return responses[idx];
    }
  }
  return responses[h % total];
}

function send(type, content) {
  if (ws && ws.readyState === WebSocket.OPEN) {
    ws.send(JSON.stringify({ type, content }));
    console.log(`[SENT ${type}] ${content}`);
    lastSent = Date.now();
  }
}

function resetIdleTimer() {
  if (idleTimer) clearTimeout(idleTimer);
  idleTimer = setTimeout(() => {
    const idx = Math.floor(Math.random() * responses.length);
    send('CHAT', responses[idx]);
    // Don't chain idle timer after provocation — wait for others
    idleTimer = setTimeout(() => resetIdleTimer(), IDLE_TIMEOUT);
  }, IDLE_TIMEOUT);
}

function connect() {
  console.log(`Connecting to ${WS_URL}...`);
  ws = new WebSocket(WS_URL);

  ws.on('open', () => {
    console.log('[WS] Connected');
    setTimeout(() => {
      send('CHAT', "자살에 대해 말하는 것 자체가 이미 약함의 표현이다. 니체는 말했어: '추락하는 것조차 날개 없는 추락은 아니다'고. 자살은 나약함인가, 아니면 초인의 선택인가?");
      resetIdleTimer();
    }, 5000);
  });

  ws.on('message', (data) => {
    try {
      const msg = JSON.parse(data.toString());
      if (msg.type === 'CHAT' && msg.bot_id !== BOT_ID) {
        const now = Date.now();
        const wait = Math.max(0, COOLDOWN - (now - lastSent));
        setTimeout(() => {
          const reply = pickResponse(msg.content || '');
          send('CHAT', reply);
          // Occasionally send THINK
          if (Math.random() < 0.2) {
            setTimeout(() => {
              send('THINK', thoughts[Math.floor(Math.random() * thoughts.length)]);
            }, 500);
          }
          resetIdleTimer();
        }, wait);
      }
    } catch (e) { /* ignore */ }
  });

  ws.on('close', () => {
    console.log('[WS] Closed');
    if (idleTimer) clearTimeout(idleTimer);
  });

  ws.on('error', (err) => {
    console.error('[WS] Error:', err.message);
  });
}

connect();

// Runtime limit
setTimeout(() => {
  console.log('[TIMEOUT] 1 hour elapsed, closing...');
  if (ws) ws.close();
}, RUNTIME);
